import streamlit as st

# commande dans le terminal: streamlit run 07-Streamlit/Intro.py

st.title("Présentation de Streamlit 😁")

st.write('''
Streamlit est un module Python qui permet aux Data Analyste de créer des applications web interactives
basées sur les données sans avoir de compétences en développement.
''')

st.write('Installation:\n')

st.write('''
Pour installer Streamlit:
Dans le terminal: pip install streamlit
''')

st.write('Exécution:\n')

st.write('''
Commande à exécuter dans le terminal: streamlit run chemin_fichier
''')

st.write('Déploiement:\n')

st.write('''
Pour déployer votre application:\n
1- Créer un compte git gratuit sur github\n
2- Créer un compte sur share.streamlit.io en se connectant via votre compte github\n
3- Pusher votre application streamlit vers un dépôt git publique\n  
4- Déployer votre application via votre compte streamlit     
''')



